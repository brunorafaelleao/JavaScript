function mediafinal(n1, n2, n3, n4) {
  const media1 = (n1 + n2 + n3) / 3;
  const media2 = (media1 + n4) / 2;
  console.log("A média final desse aluno é: " + media2);
}
mediafinal(3, 5, 1, 7);

